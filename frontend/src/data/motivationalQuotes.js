/**
 * Returns a stable but different quote index for each user.
 * Uses a fast string hash so no two users share the same quote at any moment.
 */
function hashString(str) {
  let hash = 0
  for (let i = 0; i < str.length; i++) {
    hash = ((hash << 5) - hash + str.charCodeAt(i)) | 0
  }
  return Math.abs(hash)
}

/**
 * Pick a quote deterministically from:
 *   userId    — hashed to offset the pool per user (different users see different quotes)
 *   seed      — increments per visit/session-complete (same user advances over time)
 */
export function pickQuote(userId = '', seed = 0) {
  const userOffset = hashString(userId)
  const index = (userOffset + seed) % QUOTES.length
  return QUOTES[index]
}

const SEED_KEY = 'quote_seed'

export function getQuoteSeed() {
  return parseInt(localStorage.getItem(SEED_KEY) || '0', 10)
}

export function bumpQuoteSeed() {
  const next = getQuoteSeed() + 1
  localStorage.setItem(SEED_KEY, String(next))
  return next
}
